<?php

    $function = $_POST['function'];
    
    $log = array();

    switch($function) {
        case('getState'):
        if(file_exists('chat.txt')){
          $lines = file('chat.txt');
        }
        $log['state'] = count($lines); 
        break;	
       
        case('update'):
        	$state = $_POST['state'];
        	if(file_exists('chat.txt')){
        	   $lines = file('chat.txt');
        	 }
        	 $count =  count($lines);
        	 if($state == $count){
        		 $log['state'] = $state;
        		 $log['text'] = false;
        		 
        		 }
        		 else{
        			 $text= array();
        			 $log['state'] = $state + count($lines) - $state;
        			 foreach ($lines as $line_num => $line)
                       {
        				   if($line_num >= $state){
                         $text[] =  $line = str_replace("\n", "", $line);
        				   }
         
                        }
        			 $log['text'] = $text; 
        		 }
        	  
             break;
   }
   echo json_encode($log);

   ?>